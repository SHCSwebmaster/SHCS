    function reloadCaptcha() { 

      $("#captcha-image").attr("src","php/captcha/image.php?r=" + Math.random()); 

    }

    function sendNotification() {

        var submit_button = $("#submit");

        if (submit_button.text() == submit_button.data("status-sending")) {

            submit_button.text(submit_button.data("status-original"));

        } else {

            submit_button.data("status-original", submit_button.text());
            submit_button.text(submit_button.data("status-sending"));

        }

    }

    $('#refresh-captcha').click(function(e) {

          e.preventDefault();
          reloadCaptcha();
        
    });
        
